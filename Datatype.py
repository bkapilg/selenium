#DataTypes

#Text Type:	str
#Numeric Types:	int, float, complex
#Sequence Types:	list, tuple, range
#Mapping Type:	dict
#Set Types:	set, frozenset
#Boolean Type:	bool
#Binary Types:	bytes, bytearray, memoryview

#str Data Type
x = "Hello World"
#display the data type of x:
print(type(x))

#int Data type
x = 20
#display the data type of x:
print(type(x))

#float Data type
x = 20.5
#display the data type of x:
print (x)
print(type(x))

#complex DataType
x = 1j
#display the data type of x:
print(type(x))

#List Data Type
x = ["Raj", "Durga", "Keerthana"]
#display the data type of x:
print(type(x))

#Tuple Data Type
x = ("Sai", "Sree", "Sneha")
#display the data type of x:
print(type(x))

#Range Data Type
x = range(6)
#display the data type of x:
print(type(x))

#Dict Data Type
x = {"name" : "John", "age" : 18, "School": "DAV"}

#display the data type of x:
print(type(x))

#bool DataType
x = True
#display the data type of x:
print(type(x))

